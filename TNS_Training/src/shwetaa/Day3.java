package shwetaa;
public class Day3 {
	public static void main(String[] args) {
		
		//widening or inplicit type casting
		byte b=10;
		int i=b;
		System.out.println(i);
		
		char ch='A';
		int n=ch;
		System.out.println(n);

		
		
		//narrowing or explicit type casting3g
		
		double f1= 10.52f;
		long l1= (long) f1;
		System.out.println(l1);
		
		long L2= 767352344;
		int i2= (int)L2;
		System.out.println(i2);
		
		float f2 = 34.56f;
		int i123=(int) f2;
		System.out.println(i123);



		
		
		
	}
	

}
